package workshop;

public class Point {

    private int x;
    private int y;

    public Point(final int x, final int y) {
        this.x = x;
        this.y = y;
    }
    /**
     * Returneaza x.
     * @return
     */
    public int getX() {
        return x;
    }
    /**
     * Returneaza y.
     * @return
     */
    public int getY() {
        return y;
    }

}
